import axios from 'axios';

import { API_URL } from '@/app/constants';

const api = axios.create({
    baseURL: API_URL,
});

interface AutoGeneratedText {
    main_category_id: number;
    category_name: string;
    auto_generated_texts: string[];
    final_content: string | null;
}

export interface FinalEvaluationResponse {
    student_id: number;
    student_name: string;
    category_evaluations: AutoGeneratedText[];
    overall_evaluation: string | null;
}

export const getFinalEvaluation = async (studentId: number): Promise<FinalEvaluationResponse> => {
    const response = await api.get(`/api/v1/checklist/final-evaluation/${studentId}`, {
    });

    return response.data;
}; 